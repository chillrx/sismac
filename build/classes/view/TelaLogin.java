    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        if(!nameField.getText().equals("") && !ageField.getText().equals("") && !genderField.getText().equals("") && !ratingField.getText().equals("") && !hourField.getText().equals("")){
            arquivo = new File("C:\\Users\\Chill\\Documents\\pacientes.txt");
            try {
                FileWriter inserindo = new FileWriter(arquivo, true);
                inserindo.write(nameField.getText() + "$" + ageField.getText()+ "$" + hourField.getText()+ "$"
                + genderField.getText()+ "$" + ratingField.getText() +"\r\n");
                inserindo.close();
            } catch (IOException ex) {
                Logger.getLogger(gui.class.getName()).log(Level.SEVERE, null, ex);
            }
            String aux = "Nome: " + nameField.getText() + " |" + " Idade: " + ageField.getText()+ " |" + " Hora do atendimento: " + hourField.getText()+ " |" +
                    " Classificação: " + ratingField.getText() + " |" + " Gênero: " + genderField.getText();
            listaPacientes.addElement(aux);
            listField.setModel(listaPacientes);
            nameField.setText("");
            hourField.setText("");
            genderField.setText("");
            ageField.setText("");
            ratingField.setText("");
        }
    }//GEN-LAST:event_registerButtonActionPerformed

