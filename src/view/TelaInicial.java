    private void endVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_endVendaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) VendaTabela.getModel();
        int colP = modelo.findColumn("Produto");
        int colQtd = modelo.findColumn("Quantidade");

        for (int i = 0; i < VendaTabela.getRowCount(); i++) {
            String nome = VendaTabela.getValueAt(i, colP).toString();
            double qtd1 = Double.parseDouble(VendaTabela.getValueAt(i, colQtd).toString());
            int qtd = (int) qtd1;
            ProdutoDAO produtoDao = new ProdutoDAO();
            Produto novo = new Produto();
            for (Produto p : produtoDao.read()) {
                if (p.getName().equals(nome)) {
                    novo = p;
                    novo.setStorage(p.getStorage() - qtd);
                    produtoDao.setstorage(novo);
                }
            }
        }
        modelo.setNumRows(0);
        Total.setText("0");
        readJTable();
    }//GEN-LAST:event_endVendaActionPerformed

