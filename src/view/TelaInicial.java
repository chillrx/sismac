    private void addVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addVendaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) VendaTabela.getModel();
        String produto = ComboProdutos.getSelectedItem().toString();
        ProdutoDAO produtoDao = new ProdutoDAO();
        double price = 0;
        for (Produto p : produtoDao.read()) {
            if (p.getName().equals(produto)) {
                price = p.getPrice();
            }
        }
        modelo.addRow(new Object[]{
            ComboProdutos.getSelectedItem(),
            price,
            QtdProduto.getValue(),});
        double qtd = Double.parseDouble(QtdProduto.getValue().toString());
        double total = Double.parseDouble(Total.getText()) + price * qtd;
        Total.setText(String.valueOf(total));
    }//GEN-LAST:event_addVendaActionPerformed

