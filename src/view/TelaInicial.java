    private void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) TabelaProdutos.getModel();
        modelo.setNumRows(0);

        ProdutoDAO produtoDao = new ProdutoDAO();

        for (Produto p : produtoDao.read()) {
            modelo.addRow(new Object[]{
                p.getId(),
                p.getName(),
                p.getPrice(),
                p.getStorage(),});

            ComboProdutos.addItem(p.toString());
        }

        DefaultTableModel modelouser = (DefaultTableModel) tabelaUsuarios.getModel();
        modelouser.setNumRows(0);

        UsuarioDAO usuarioDao = new UsuarioDAO();

        for (Usuario u : usuarioDao.read()) {
            modelouser.addRow(new Object[]{
                u.getId(),
                u.getName(),
                u.getLogin(),
                u.getPassword(),});
        }

        DefaultTableModel modelocliente = (DefaultTableModel) tabelaClientes.getModel();
        modelocliente.setNumRows(0);

        ClienteDAO clienteDao = new ClienteDAO();

        for (Cliente c : clienteDao.read()) {
            modelocliente.addRow(new Object[]{
                c.getId(),
                c.getName(),
                c.getPhonenumber(),
                c.getBirthdate(),
                c.getEmail(),});
        }

    }

